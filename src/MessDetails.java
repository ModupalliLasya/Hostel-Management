    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String id = jTextField1.getText();
    try {
        Connection con = ConnectionProvider.getCon();
        Statement st = con.createStatement();
        ResultSet rs = st.executeQuery("SELECT name FROM student WHERE student_id = '" + id + "'");
        if (rs.next()) {
            jTextField2.setText(rs.getString("name"));
        } else {
            JOptionPane.showMessageDialog(null, "Student ID not found");
            clear(); // Clear all fields if student ID not found
            return; // Exit the method
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
    }

    // Set the amount based on the selected mess option
    Object selectedOption = jComboBox1.getSelectedItem();
    if (selectedOption != null) {
        String messOption = selectedOption.toString();
        String amount = "";
        switch (messOption) {
            case "Veg Mess":
                amount = "65000"; // Set the amount for veg mess
                break;
            case "Non Veg Mess":
                amount = "81000"; // Set the amount for non-veg mess
                break;
            case "Special Mess":
                amount = "92000"; // Set the amount for special mess
                break;
            default:
                amount = ""; // Clear the amount if no mess option is selected
                break;
        }
        jTextField3.setText(amount); // Set the amount in the text field
    } else {
        JOptionPane.showMessageDialog(null, "Please select a mess option");
    }
    }//GEN-LAST:event_jButton4ActionPerformed


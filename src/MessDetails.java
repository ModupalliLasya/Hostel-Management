    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int confirmation = JOptionPane.showConfirmDialog(null, "Do you want to confirm the payment?", "Confirmation", JOptionPane.YES_NO_OPTION);
        if (confirmation == JOptionPane.YES_OPTION) {
            String studentId = jTextField1.getText();
            try {
                Connection con = ConnectionProvider.getCon();
                Statement st = con.createStatement();
                ResultSet rs = st.executeQuery("SELECT * FROM mess WHERE student_id = '" + studentId + "'");
                if (rs.next()) {
                    JOptionPane.showMessageDialog(null, "Payment is already done");
                    clear();
                } else {
                    String messType = jComboBox1.getSelectedItem().toString();
                    String amount = jTextField3.getText();
                    String paymentStatus = "Paid";
                    String insertQuery = "INSERT INTO mess (student_id, mess_type, amount, pay_status) VALUES (?, ?, ?, ?)";
                    PreparedStatement insertStatement = con.prepareStatement(insertQuery);
                    insertStatement.setString(1, studentId);
                    insertStatement.setString(2, messType);
                    insertStatement.setString(3, amount);
                    insertStatement.setString(4, paymentStatus);
                    int rowsAffected = insertStatement.executeUpdate();
                
                    if (rowsAffected > 0) {
                        JOptionPane.showMessageDialog(null, "Payment done successfully");
                        clear();
                    } else {
                    JOptionPane.showMessageDialog(null, "Failed to process payment");
                    }
                }
            } 
            catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

